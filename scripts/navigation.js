function navigatePage(location) {
    switch(location) {
        case 'mainPage':
            window.location.href="./index.html"
            break;
        case 'pedigreePage':
            window.location.href="./pedigrees.html"
            break;
        case 'spotifyPage':
            window.location.href="./spotify.html"
            break;
        case 'aboutPage':
            window.location.href="./about/about.html"

    }
}